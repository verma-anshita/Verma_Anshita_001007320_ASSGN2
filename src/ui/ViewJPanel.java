    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex= tblView.getSelectedRow();
        
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }
        
        DefaultTableModel model=(DefaultTableModel)tblView.getModel();
        Car selectedCars=(Car) model.getValueAt(selectedRowIndex, 0);
        
        details.deleteCars(selectedCars);
        
        JOptionPane.showMessageDialog(this, "Car Details deleted");
        
        populateTable();
        details.setTimeStamp(new Date());
        lblTimestamp.setText(details.getTimeStamp().toString());
        
        txtCompany.setText("");
        chkavailable.setSelected(false);
        txtManufacturingYr.setText("");
        txtseats.setText("");
        txtSerialNo.setText("");
        txtModelNo.setText("");
        chkRegistered.setSelected(false);
        chkCertificate.setSelected(false);
        txtCity.setText("");
        
    }//GEN-LAST:event_btnDeleteActionPerformed

