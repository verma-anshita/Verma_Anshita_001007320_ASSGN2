    private void populateTable() {
        //To change body of generated methods, choose Tools | Templates.
        DefaultTableModel model=(DefaultTableModel)tblView.getModel();
        model.setRowCount(0);
        for(Car cr:details.getDetails()){
            Object[] row=new Object[10];
            row[0]= cr;
            row[1]=cr.isAvailable();
            row[2]=cr.getManufacturedYear();
            row[3]=cr.getNumberOfSeat();
            row[4]=cr.getSerialNumber();
            row[5]=cr.getModelNumber();
            if(cr.isRegUber()){
            row[6]= "YES";
                    }
            else{
                row[6]="NO";
            }
            if(cr.isMtCertificate()){
            row[7]= "EXPIRED";
                    }
            else{
                row[7]="VALID";
            }
            row[8]=cr.getCity();
            model.addRow(row);
        }
       
        if(details.getTimeStamp() != null)
        {
           lblTimestamp.setText(details.getTimeStamp().toString());
        }
    }
