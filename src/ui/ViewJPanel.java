    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
       
        
        int selectedRowIndex= tblView.getSelectedRow();
        
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row and click View to Update.");
            return;
        }
        
        DefaultTableModel model=(DefaultTableModel)tblView.getModel();
        Car selectedCar=(Car) model.getValueAt(selectedRowIndex, 0);
       
        
         String company=txtCompany.getText();
         boolean available= chkavailable.isSelected();
         String mnyear=txtManufacturingYr.getText();
         String modelNo=txtModelNo.getText();
         boolean register=chkRegistered.isSelected();
         boolean certificate=chkCertificate.isSelected();
         String city=txtCity.getText();
         int seats = 0;
        

         
         if(!(txtseats.getText()==null||txtseats.getText()==""||txtseats.getText().isBlank()||txtseats.getText().isEmpty()))
         {
              seats=Integer.parseInt(txtseats.getText());
           }
        
        selectedCar.setCompany(company);
        selectedCar.setCity(city);
        selectedCar.setManufacturedYear(mnyear);
        selectedCar.setModelNumber(modelNo);
        selectedCar.setNumberOfSeat(seats);
        selectedCar.setMtCertificate(certificate);
        selectedCar.setRegUber(register);
        
        details.updateCar(selectedCar);
        
        JOptionPane.showMessageDialog(this, "Row Updated, Please Reload page to see changes.");
        
        details.setTimeStamp(new Date());
        lblTimestamp.setText(details.getTimeStamp().toString());
        
         txtCompany.setText("");
         chkavailable.setSelected(false);
         txtManufacturingYr.setText("");
         txtseats.setText("");
         txtSerialNo.setText("");
         txtModelNo.setText("");
         chkRegistered.setSelected(false);
         chkCertificate.setSelected(false);
         txtCity.setText("");
         
         txtCompany.setEditable(false);
       
         txtManufacturingYr.setEditable(false);
         txtseats.setEditable(false);
         txtSerialNo.setEditable(false);
         txtModelNo.setEditable(false);
        
         txtCity.setEditable(false);
         
          btnUpdate.setEnabled(false);
        
    }//GEN-LAST:event_btnUpdateActionPerformed


